package presupuesto.bean;

/**
 * @author CARLOS SANTANDER
 */
public class ExpedienteNota {

    private String ano_eje;
    private String sec_ejec;
    private String expediente;
    private String ciclo;
    private String fase;
    private String secuencia;
    private String secuencia_nota;
    private String notas;
    private String estado_envio;

    public ExpedienteNota() {
    }

    public String getAno_eje() {
        return ano_eje;
    }

    public void setAno_eje(String ano_eje) {
        this.ano_eje = ano_eje;
    }

    public String getSec_ejec() {
        return sec_ejec;
    }

    public void setSec_ejec(String sec_ejec) {
        this.sec_ejec = sec_ejec;
    }

    public String getExpediente() {
        return expediente;
    }

    public void setExpediente(String expediente) {
        this.expediente = expediente;
    }

    public String getCiclo() {
        return ciclo;
    }

    public void setCiclo(String ciclo) {
        this.ciclo = ciclo;
    }

    public String getFase() {
        return fase;
    }

    public void setFase(String fase) {
        this.fase = fase;
    }

    public String getSecuencia() {
        return secuencia;
    }

    public void setSecuencia(String secuencia) {
        this.secuencia = secuencia;
    }

    public String getSecuencia_nota() {
        return secuencia_nota;
    }

    public void setSecuencia_nota(String secuencia_nota) {
        this.secuencia_nota = secuencia_nota;
    }

    public String getNotas() {
        return notas;
    }

    public void setNotas(String notas) {
        this.notas = notas;
    }

    public String getEstado_envio() {
        return estado_envio;
    }

    public void setEstado_envio(String estado_envio) {
        this.estado_envio = estado_envio;
    }

}
